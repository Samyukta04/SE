#include <iostream>
using namespace std;

int main() {
    int n1, n2, sum;

    cout << "Enter the first number: ";
    cin >> n1;

    cout << "Enter the second number: ";
    cin >> n2;

    sum = n1 - n2;

    cout << "The sum of " << n1 << " and " << n2 << " is " << sum << "." << endl;

    return 0;
}
